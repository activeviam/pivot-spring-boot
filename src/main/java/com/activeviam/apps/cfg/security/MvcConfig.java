package com.activeviam.apps.cfg.security;

import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.ViewControllerRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class MvcConfig implements WebMvcConfigurer {

	@Override
	public void addViewControllers(ViewControllerRegistry registry) {
		registry.addViewController("/kerb/home").setViewName("home");
		registry.addViewController("/kerb").setViewName("home");
		registry.addViewController("/kerb/hello").setViewName("hello");
		registry.addViewController("/kerb/login").setViewName("login");
	}

}